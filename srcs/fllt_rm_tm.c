#include <fillit.h>

void			fllt_rm_tm(t_box *box, t_tm rm)
{
	int			i;

	i = 0;
	while (box->str[i])
	{
		if (box->str[i] == 'A' + rm.s.order)
			box->str[i] = '.';
		i++;
	}
}
